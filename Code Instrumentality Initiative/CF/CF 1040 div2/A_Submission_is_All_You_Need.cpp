constexpr bool more = true;

// Codeforces Round 1040 (Div. 2) 
// A. Submission is All You Need
// https://codeforces.com/contest/2130/problem/A
// 贪心
void HuanF() {
    int n;
    cin >> n;
    int ans = 0;
    for (int i = 0, x; i < n; ++i) {
        cin >> x;
        if (x == 0) x = 1;
        ans += x;
    }
    cout << ans << "\n";
}

signed main() {
    init();
    int T = 1;
    if (more) cin >> T;
    while (T-- > 0) HuanF();
    return 0;
}
